package it.polito.tdp.rivers.model;

public class Event {
	
	private double fin;
	private double fout;
	
	public Event(double fin, double fout) {
		super();
		this.fin = fin;
		this.fout = fout;
	}
	
	public double getFin() {
		return fin;
	}
	public double getFout() {
		return fout;
	}
	
	

}
